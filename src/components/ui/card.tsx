import * as React from "react";

import { cn } from "@/lib/utils";

/**
 * Neo-Brutalism Card Component
 *
 * 특징:
 * - 두꺼운 검은 보더 (3px)
 * - 하드 섀도우 (blur 없는 offset shadow)
 * - 평평한 배경색
 * - 선명한 경계
 */

