  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      // Base styles
      "bg-white text-black",
      // Brutal border
      "border-[3px] border-black",
      // Hard shadow
      "shadow-brutal",
      className
    )}
    {...props}
  />
));
Card.displayName = "Card";

